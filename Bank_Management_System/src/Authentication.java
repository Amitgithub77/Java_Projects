    private void btnLoginActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnLoginActionPerformed
       String sql = "SELECT * FROM account_table WHERE Acc_no=? and Pin=?";
       try{
           
           
           pst=conn.prepareStatement(sql);
           pst.setString(1, txtfAccountNo.getText());
           pst.setString(2, txtfPin.getText()); 
           rs=pst.executeQuery();
           
           if(rs.next()){
               setVisible(false);
               Loading obj = new Loading();
               obj.setUpLoading();
               obj.setVisible(true); 
               rs.close();
               pst.close();
           }else{
               JOptionPane.showMessageDialog(null, "Invalid Credentials..Please Try Again");
               txtfAccountNo.setText("");
               txtfPin.setText("");
               jTextField3.setText("");
           }
           
       }catch(Exception ex){
           ex.printStackTrace();
           JOptionPane.showMessageDialog(null, ex); 
       }finally{
           try{
               rs.close();
               pst.close();
           }catch(Exception e){
               
           }
       }
        
    }//GEN-LAST:event_btnLoginActionPerformed

